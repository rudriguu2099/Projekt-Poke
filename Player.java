public class Player{
    private String name;
    private int x, y;

    //Constructor
    public Player(String name, int x, int y) {
        this.name = name;
        this.x = x;
        this.y = y;
    }

    // Get'n Set Methods
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    //Methods


    
    
}